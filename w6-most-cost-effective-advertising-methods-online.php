					And it’s especially important 
					if you’re a B2B company, because networking becomes an <br/>important way to <b>generate word-of-mouth buzz</b>
					about your product or service, meet <br/>potential customers or clients, and become better informed about market conditions in<br/> your industry. 
					</p><br/><a href="http://imgur.com/BnUDxT7"><img src="http://i.imgur.com/BnUDxT7.jpg  title="source: imgur.com" /></a><br/>
					
					
					<b style="font-size:35px">What's B2B?</b><br/><br/>
					<p>Look<br/><br/>To understand the real benefits of business networking, though, you first have to<br/> understand 
					the differences between B2B (business-to-business) and B2C (business-to-consumer) companies.
					<br/><br/>What’s B2B? It’s simply a shorthand way of indicating that your business sells to other <br/> businesses rather than to consumers. Instead of selling to a consumer in a store or retail <br/>location, for example, you are selling to an employee in the division or department of<br/> another company.
					</p><br/><br/>
					
					<p style="background-color:powderblue; padding:0.5cm 0.5cm; border-style: solid; border-width:1px"><b>One example of a B2B company</b> is a new tech startup that is looking for companies to adopt
					and use its software within the enterprise. This might be software to help <br/>automate some part of
					the business, such as new marketing software that makes it <br/>possible to track the conversations and 
					discussions taking place about that company <br/>on social media.
					</p><br/><br/>
					
					<p>You can immediately see why networking is so important in this case -- since the product 
					(the piece of software) isn’t being sold in stores and it’s probably not being marketed on the internet, 
					most people probably haven’t heard about your product or fully understand how it can help to solve an important business need. 
					</p><br/><br/>
					
					<p><b style="font-size:35px">Here is what you should do</b></p><br/>
					<p style="background-color:#F3F781; padding:0.5cm 0.5cm; border-style: solid; border-width:1px">(a) informing people what your company does <br/>(b) explaining what types of 
					solutions you offer and<br/> (c) giving people a way to sample your product, such as through a live demo.
					</p><br/><br/>
					<a href="http://imgur.com/NqKyn3V"><img src="http://i.imgur.com/NqKyn3V.jpg" title="source: imgur.com" /></a>
					<p><b style="font-size:35px">Where can I do that?</b><br/><br/>

					•	<a href="https://www.biscle.com" style="text-decoration:none; color:#ff0000;">Online Social networks</a> such as <a href="https://www.linkedin.com" style="text-decoration:none; color:#ff0000;">Linkedin</a>, <a href="https://www.biscle.com" style="text-decoration:none; color:#ff0000;">Biscle</a>, <a href="https://www.xing.com" style="text-decoration:none; color:#ff0000;">Xing</a><br/>
					•	Industry niche websites and communities<br/>
					•	In-person events like meet-ups<br/>
					•	Trade shows and exhibitions<br/>
					•	Business Conferences<br/>
					•	Alumni events<br/>

					</p><br/><br/>
					
					<p>Each of these different ways of networking can be used for a different purpose. <br/>For example, 
					you might use trade shows and exhibitions to show people your product in a<br/> physical setting. But you’d use
					online networking in a different way, such as talking with <br/>other entrepreneurs about how a new regulation or piece of legislation might impact your <br/>business.
					</p><br/><br/>
					
					<p><b style="font-size:35px">What can I achieve?</b><br/><p>The connections you make at in-person events are so important because, unlike a B2C <br/>company, you can’t expect a lot of passionate customers to 
					do all the heavy lifting for you. <br/>So you need to get out in front of other B2B executives, 
					and make your case. But don’t think that B2B networking is all about pitching and selling. There are many different reasons to <br/>network.
					</p><br/><br/>
					
					
					<p style="background-color:#D0F5A9; padding:0.5cm 0.5cm; border-style: solid; border-width:1px">•	To meet new partners<br/>
					•	To become acquainted with vendors and/or suppliers<br/>
					•	To meet future employees<br/>
					•	To meet potential investors<br/>
					•	To learn about the industry<br/>
					•	To find a mentor or coach<br/>

					</p><br/><br/>
					
					<p>And now<br/><br> networking for B2B entrepreneurs is especially important if you are looking to expand into global markets. Say, for example, 
					you’re a U.S.-based business that’s looking to tap into <br/>some of the tremendous economic growth in Asia (and especially China). You might attend 
					networking events especially for importers and exporters. Along the way, you might get a <br/>much better idea of what types of hurdles or obstacles you might expect along the way 
					as <br/>you expand operations abroad. Since every country's culture is different thus the need for <br/>specific product may vary.
					</p><br/><br/>
					
					<p>As you can see, there are many benefits of business networking for B2B entrepreneurs. 
					The internet has made it easier than ever before to connect and meet like-minded business owners who 
					are also looking to grow their business.
					</p><br/><br/>
					
					
