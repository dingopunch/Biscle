					<p><b style="font-size:35px">Youtube Videos</b><br/><br/>
					Everyone should be able to create a video since every smartphone is equipped with a camera. Video is the most effective way to visually present your product to your customer. 
					It doesn’t matter if you are doing a product presentation or a commercial, a video showing your company or product will allow your customers to understand you easily.
					Youtube isn’t just a website where you can upload videos. Youtube is a hub / a social network. When millions of people are watching cats and dogs online, sometimes they find your video.
					But how do I get noticed?
					<br/><br/>
					1. If you want to get noticed, first you have make your video interesting and have quality content.
					<br/>
					Get your microphone sound loud, get your video clear, and present your content easy to understand this is the basic.
					<br/><br/>
					2. Don’t be normal. Normal is the last thing you want to do in the world of marketing. Have compelling titles and content.
					<br/>
					"I am selling a notebook that is biodegradable."  booorrrring!!!!!
 
					“The Ultimate Notebook that saves the world from environmental hazard”
					I wonder what that is.
					<br/><br/>
					3. Discover your audience. <br/>
					Not everyone is going to like your product or your video, but spreading the message and target to those who care is what we are aiming for. 
 
					But I don’t know how to be different….
 
					Check out this video that went viral in 2016. Its call apple pen pineapple pen, maybe you can learn something from it.
 
					*Should I pay ads on youtube? People don’t really like ads on youtube, but if you think your content is good enough that people are willing to click on when they are having fun watching cute pets then why not.
					</p><br/><br/>
					
					<a href="http://imgur.com/jBV1pbN"><img src="http://i.imgur.com/jBV1pbN.jpg" title="source: imgur.com" /></a>
					
					
					<p><b style="font-size:35px">Product Hunt</b><br/><br/>
 
					Product hunt is a site where users can upload their product. It works kind of like reddit. Users can upvote products 
					they like and the product with the most votes rise to the top of each day’s list. <br/>
 
					The site features mostly tech savvy products like mobile apps, games, IoT products and books. 
					If you are selling physical then we can jump to the next method. <br/>

					Product hunt is the perfect place if you want to test the water of your ideas, and prototype. You can get feedbacks from users,
					and market your prototype at the same time before an official launch. You can also answer question that people may have to improve
					your product and build a relationship with your users.
					</p><br/><br/>
					
					<p><b style="font-size:35px">B2B / B2C websites</b><br/><br/>
 
					We are all familiar with B2C sites like Ebay, and Amazon, Shopify. For those two platform only ebay allows you to sell your product for free. But they make you 
					advertise your product on their platform otherwise you will not receive any attention on their platform. B2C platforms have become incredibly competitive because 
					anyone can sell something online. A tip here is to make your product keywords incredibly niche. Instead of just using “product” as your keyword, put on more details 
					like the specification, color, price, manufacturing place for the keywords. Users are more likely to find your items when search more specifically in the search engine.Still, 
					don’t expect people will find your product on B2C websites without any advertising. 

					A simple way is have people do reviews about your product and use social network, youtube videos to drive traffic to your shop.
 
					As for the B2B vendors, sites like Alibaba, Biscle is a good way to market your product. Both sites allow users to post their product, company profile connect to sellers or suppliers online.
 
					If you would like to sell your product internationally online, then you should try to find out the local B2C websites for that region (eg. Rakuten(Japan), Taobao(China) or Local B2B websites (eg. 1688(China). Etc.
 
					There is always a platform for anything.


					</p><br/><br/>
					
					<p><b style="font-size:35px">Adwords</b><br/><br/>
 
 
					Many people know about adwords but they don’t have the budget for it. The bidding prices are very competitive and most of the time the
					keywords that you want to buy are too expensive. I mean who would spend 5 dollars for a click. If you want to get 1000 clicks a day then 
					$5000 will be gone in a flash.<br/><br/>
 
 
					How do I get 1000 clicks within 30 dollars? That's 3 cents a click?
 
					The truth is you may not get all the keywords you want exactly, but you can get somewhat similar keywords that may target the same audience.
 
					1. Goto keyword planner start a new campaign building a new of keywords<br/>
					
					2. Select as many keywords as you can from the planner, but always try to find relevant keywords and the cheapest keywords into your campaign.
					It is ok for some keywords to have very few searches because when you add them all up, it will eventually add up to a large amount.<br/>
					<a href="http://imgur.com/MEBrlnF"><img src="http://i.imgur.com/MEBrlnF.jpg" title="source: imgur.com" /></a>
					
					3. Set your bid price starting from 1 cent. Adwords will put your ads in the search result even if your bid is low, the catch is that it may not
					always appear on the first page of search. But that is ok, since Adwords only charges you when the user click into your website, so all you lose is time. <br/>
					
					4.If you are not getting any clicks or impression from adwords then you can start adding your bid to the adwords campaign. Depending on your budget,
					you can slowly add your bid from 2cents - 20 cents. But be careful because the campaign becomes expensive quick.<br/><br/>
 
					Bonus: Adwords is a way to build your seo campaign. If your site is about spa, write an article about spa, then include your product, videos, 
					website backlinks  in it and eventually it will help you grow your presence in seo.<br/>
					</p><br/><br/>
					
					<p><b style="font-size:35px">5. Websites/ social network /facebook/ twitter / instagram</b><br/><br/>
 
					Please tell me you have a website. If you don’t then you should at least have a facebook page.
 
					In 2017, if you don’t have a website or a facebook page, you are not qualified to be in business. Online presence is like the social security card for your business. 
					People don’t trust business that doesn’t have a website because it may seem kind of shady and unprofessional.
 
					If you have a facebook page, please upload content daily to show you are in business. You can also use professional networks like linkedin, Biscle to build your profile so people 
					may find you on the web when searching related product or industry.

					Content - you need to create posts that has a call to action to your product.
					Content marketing has become seamlessly that we don’t even recognize when a piece of content is an advertisement.

					If you are selling a pet product, then you should write articles about pets. For example, if  you sell organic pet food. You can write an article like” After experimenting
					with organic cat food, my cat’s fur has become more shiny and smooth.”<br/><br/>

					Or <br/><br/>

					The 5 important ways of keeping your pet healthy and happy

					Your viewer/customer must find your content valuable to them. 

					Since Social network is about sharing “content” and growing a community, the more useful the content is, the more attention your product will receive traffic. 
					On top of that, the content that has been shared and viewed a lot will rank better in seo.

					Do not keep promoting your product because people get tired of it on the second time.  If you don’t always have new content then share other people’s content on your page. 
					Also, you can share interesting images that are related to your business. I'm sure people will appreciate your cute little kitten.

					These people aren’t necessarily all your customer. But they are all your audience. Which means that somehow, these people will become a promoter for your business.
					You should be the curator of your page and answer questions people may have. 

					Like I mentioned above, youtube is also a social network. People who has grown a lot of subscribers to their channels are the people who creates great content. 
					When you grow your subscriber base to a certain amount, the influence will be astonishing. 

					</p><br/><br/>
					
					
					<p><b style="font-size:35px">Crowdfunding </b><br/><br/> 
 
					There is no doubt that crowdfunding has become the trend to market and sell product. By pitching your idea online to millions of users, crowdfunding campaigns can 
					1. Advertise your product directly for free. 
					2.find out if anyone is interested in your product. 
					3. Fund your first batch of your product if the campaigns succeed.
 
					The most notable crowdfunding sites are definitely Kickstarter and indiegogo. Startups, hardware developer, designers are all using these amazing platform to
					test the water.These platforms are also consider B2C websites since it deals with consumer directly.
	
					If the campaign fails, you still don’t lose anything. But if you succeed, you will get order and possibly go viral online and be featured in social media or news.
 
					How to launch a successful campaign?
 
					1. Unconventional product
					Users from crowdfunding platforms are often looking for innovative product that hasn’t been in the market yet. If your targeted consumers can’t find your product
					at local market retail store, they will probably be curious about what you have to offer.
 
					2.   	Solve a problem
	
					Unless you have a really fun game, an awesome movie, a very stylish pen, otherwise you better help your customers solve a problem that bothers them on a daily basis.

					3.  Create great video, commercial about your product.
 
					Crowdfunding buyers are more likely to fund a product that has a clear introduction. Explaining the features with eye catching visual presentation is better than bunch
					of text that are boring to read. If a picture is worth a thousand words, then a video is probably worth 1 million words. 

